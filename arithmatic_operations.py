#The basic arithmatic functions in Python.
print(10+3) #addition
print(10-3) #subtraction
print(10*3) #multiplication
print(10/3) #integral division
print(10//3) #floor division
print(10%3) #modulo operator
print(10**3) # 10 to the power 3 

y = 50
y -= 10 # Decrement of a number
print(y)

a = 50  
a += 10 # Increment of a number
print(a)

z = (10 + 3) * 4 ** 3 #Python follows BODMAS system 
print(z) 

